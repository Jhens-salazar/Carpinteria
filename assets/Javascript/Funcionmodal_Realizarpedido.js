// Función para cerrar la ventana modal
function cerrarModal() {
    const modal = document.getElementById("miModal");
    modal.style.display = "none"; // Oculta el modal cambiando la propiedad display
}
// Función para abrir la ventana modal
function abrirModal() {
    const modal = document.getElementById("miModal");
    modal.style.display = "block"; // Muestra el modal cambiando la propiedad display
}
